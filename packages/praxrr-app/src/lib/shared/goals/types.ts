/** Slider axes. Four are signed weights `[-1, 1]`; `unwantedStrictness` is one-directional `[0, 1]`. */
export type GoalWeightAxisKey = 'qualityVsSize' | 'compatibility' | 'hdrPreference' | 'unwantedStrictness';

/** The resolution ceiling is a discrete enum, not a weight. */
export type GoalResolutionCeiling = '720p' | '1080p' | '2160p';

export const GOAL_RESOLUTION_CEILINGS: readonly GoalResolutionCeiling[] = ['720p', '1080p', '2160p'] as const;

/**
 * The resolved set of slider positions. Weight axes are integers `0..100`; `resolutionCeiling` is an
 * enum. Preset defaults + user edits both produce this shape.
 */
export interface GoalWeights {
  qualityVsSize: number;
  compatibility: number;
  hdrPreference: number;
  unwantedStrictness: number;
  resolutionCeiling: GoalResolutionCeiling;
}

