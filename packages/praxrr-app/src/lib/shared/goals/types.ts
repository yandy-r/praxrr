/**
 * Profile-independent facts about one custom format, materialized from the PCD cache. Tags arrive
 * as stored (Title-Case) and are lowercased by the classifier before matching.
 */
export interface CfFacts {
  name: string;
  description: string | null;
  tags: string[];
}

/** Where the ceiling gate placed a CF relative to the chosen resolution ceiling. */
export type GoalCeilingRelation = 'above' | 'match' | 'below';

/** One additive term of a score, for transparent "base +700, quality-favored +700" rendering. */
export interface GoalAxisContribution {
  axis: GoalWeightAxisKey;
  delta: number;
}

/** Machine-readable, narration-ready rationale for one emitted score. */
export interface GoalReason {
  /** Stable code, e.g. `category.remux` or `rule.fallback.no-match`. */
  code: string;
  category: GoalCategory | null;
  ruleId: string;
  base: number;
  axisContributions: GoalAxisContribution[];
  ceiling: GoalCeilingRelation | null;
}

/** One scored custom format in the plan. */
export interface GoalCfDecision {
  customFormatName: string;
  arrType: GoalArrType;
  category: GoalCategory;
  score: number;
  reason: GoalReason;
}

/** A custom format the classifier could not place; excluded from the scoring input (never mis-scored). */
export interface GoalUncategorizedCf {
  name: string;
  /** Advisory, clearly-unvalidated best guess from the tag signal, or null. */
  suggestedCategory: GoalCategory | null;
  reason: string;
}

/** Coverage split for the transparency surface. */
export interface GoalCoverage {
  total: number;
  scored: number;
  uncategorized: number;
}

export interface GoalThresholds {
  minimumScore: number;
  upgradeUntilScore: number;
  upgradeScoreIncrement: number;
}

/** Input to the engine. Materialized by the route; the engine touches no DB. */
export interface ComputeGoalPlanInput {
  arrType: GoalArrType;
  weights: GoalWeights;
  presetBaseUpgrade: number;
  customFormats: CfFacts[];
  /**
   * Per-arr quality facts (name + resolution) materialized from `quality_api_mappings` + `QUALITIES`.
   * Optional so pure CF-only tests need not thread them; absent → no ladder change.
   */
  qualityFacts?: GoalQualityFact[];
  /** The profile's current `quality_profile_qualities` rows; absent → no ladder change. */
  currentLadder?: OrderedItem[];
  /** True when the profile is also compatible with a sibling arr (drives the shared-ladder advisory). */
  compatibleWithSiblingArr?: boolean;
}

/** The engine's deterministic output. */
export interface GoalPlan {
  engineVersion: string;
  arrType: GoalArrType;
  decisions: GoalCfDecision[];
  uncategorized: GoalUncategorizedCf[];
  thresholds: GoalThresholds;
  coverage: GoalCoverage;
  /** Ready for `buildScoringOps`; scores use an explicit arrType, never `'all'`. */
  scoringInput: UpdateScoringInput;
  /**
   * Internal desired quality ladder (analogue of `scoringInput`), consumed by the shared op builder
   * for preview + apply. `null` when the ceiling produces no ladder change for this profile.
   */
  ladderInput: { orderedItems: OrderedItem[] } | null;
  /** Wire-exposed presentational ladder + cutoff for the always-shown generated-config surface. */
  qualityLadder: GoalQualityLadder;
}

/** Per-CF score delta between two plans, powering the slider goal-diff. */
export interface GoalScoreDelta {
  customFormatName: string;
  from: number | null;
  to: number | null;
  delta: number;
}

export interface GoalThresholdDelta {
  field: keyof GoalThresholds;
  from: number;
  to: number;
}

/** Pure diff between two goal plans (e.g. before/after moving a slider). */
export interface GoalDiff {
  scoreChanges: GoalScoreDelta[];
  thresholdChanges: GoalThresholdDelta[];
}
