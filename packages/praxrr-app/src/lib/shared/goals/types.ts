/**
 * Quality Goals — intent-to-implementation bridge (issue #20).
 *
 * Pure, versioned contracts for the goals translation engine. This module holds only types,
 * enumerations, and static metadata — no logic, no I/O, no runtime/DB imports (the sole exception
 * is a type-only import of {@link UpdateScoringInput}, the shape the engine emits). It is therefore
 * safe to import from client and server alike, mirroring the `$shared/narration` precedent.
 *
 * The engine translates a user's high-level intent (a preset + slider weights) into concrete
 * custom-format scores + quality-profile thresholds — a standard {@link UpdateScoringInput} that
 * flows through the existing `buildScoringOps`/`updateScoring` op path. It never becomes a parallel
 * scoring system.
 */

import type { UpdateScoringInput } from '$pcd/entities/qualityProfiles/scoring/update.ts';
import type { OrderedItem } from '$shared/pcd/display.ts';
import type { GoalQualityFact, GoalQualityLadder } from './ladder.ts';

/**
 * Stamped onto every {@link GoalPlan} and persisted binding. Bump whenever the classifier rules,
 * the closed {@link GoalCategory} set, the policy magnitudes, or the threshold formulas change, so
 * a client can detect that a bound profile was generated by a different engine generation.
 */
export const GOALS_ENGINE_VERSION = '2';

/** Arr apps the engine can APPLY to in this slice. Lidarr is classified but not applied. */
export type GoalArrType = 'radarr' | 'sonarr';

/** The 4 built-in presets. */
export type GoalPresetId = 'best-quality' | 'smallest-size' | 'balanced' | '4k-hdr-priority';

/**
 * Closed, versioned set of semantic categories a custom format can be classified into. Adding,
 * removing, or renaming a member is a breaking change — bump {@link GOALS_ENGINE_VERSION}.
 * `null` (uncategorized) is represented outside this union.
 */
export type GoalCategory =
  | 'unwanted'
  | 'hdr_dv'
  | 'hdr_hdr10plus'
  | 'hdr_baseline'
  | 'remux'
  | 'release_group_tier_1'
  | 'release_group_tier_2'
  | 'release_group_tier_3'
  | 'audio_lossless'
  | 'audio_advanced'
  | 'audio_baseline'
  | 'streaming_service'
  | 'movie_version'
  | 'repack_proper'
  | 'resolution';

